#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	index;

	index = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (str == NULL)
		return (NULL);
	if (s && f)
	{
		while (s[index])
		{
			str[index] = f(index, s[index]);
			++index;
		}
		str[index] = '\0';
		return (str);
	}
	return (NULL);
}
